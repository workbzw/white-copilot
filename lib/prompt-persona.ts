/**
 * 返回当前生效的系统人设（环境变量优先，否则默认大唐研究院人设）。
 */
export function getSystemPersona(): string {
  const custom = process.env.LLM_SYSTEM_PERSONA?.trim();
  return custom && custom.length > 0 ? custom : DEFAULT_SYSTEM_PERSONA;
}
